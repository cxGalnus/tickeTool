public class TickeToolException extends Exception {
    public TickeToolException() {
        super();
    }

    public TickeToolException(String message) {
        super(message);
    }

    public TickeToolException(String message, Throwable cause) {
        super(message, cause);
    }

    public TickeToolException(Throwable cause) {
        super(cause);
    }

}
